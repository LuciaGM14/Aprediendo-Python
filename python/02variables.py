#variables en minúsculas.

# Variable de cadena
my_variables = "Esto es una variable"
print(my_variables)

# Variable de numeros
numero_1 = 1
print(numero_1)

# Variable de boleeanos
boleano_1 = False
print(boleano_1)

# Varias variables en una cadena
print(my_variables, numero_1, boleano_1)

#Concatenacion (simepre que se ponga coma se concatena)
print("Este es el valor de:", numero_1)

